    private void append(java.awt.event.KeyEvent evt) {
        if (evt.getKeyCode() != VK_LEFT && evt.getKeyCode() != VK_RIGHT
                && evt.getKeyCode() != VK_UP && evt.getKeyCode() != VK_DOWN && evt.getKeyChar() != '\b') {
            char key = evt.getKeyChar();
            if (key == 's' || key == 'S') {
                textArea.replaceSelection("in()");
                textArea.setCaretPosition(textArea.getCaretPosition() - 4);
            } else if (key == 'c' || key == 'C') {
                textArea.replaceSelection("os()");
                textArea.setCaretPosition(textArea.getCaretPosition() - 4);
            } else if (key == 't' || key == 'T') {
                textArea.replaceSelection("an()");
                textArea.setCaretPosition(textArea.getCaretPosition() - 4);
            } else if (key == 'l' || key == 'L') {
                textArea.replaceSelection("og()");
                textArea.setCaretPosition(textArea.getCaretPosition() - 4);
            }
            
            char check = ' ';
            if (!textArea.getText().isEmpty())
                check = (textArea.getText().charAt(textArea.getText().length() - 1));
            boolean isInteger = false;
            if ((evt.getKeyChar() >= '0') && (evt.getKeyChar() <= '9'))
                isInteger = true;
            if (isInteger && (check >= '0') && (check <= '9'))
                textArea.replaceSelection("");
            else
                textArea.replaceSelection(" ");
        }
        inputString = textArea.getText();
    }
    
